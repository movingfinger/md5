#include "libft.h"

int				ft_pow(int a, int b)
{
	if (b == 0)
		return (1);
	return (a * (ft_pow(a, b - 1)));
}

long double		ft_powf(long double a, int b)
{
	if (b == 0)
		return (1);
	return (a * (ft_powf(a, b - 1)));
}
