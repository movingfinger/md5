#include "libft.h"

void	ft_strtolower(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i])
	{
		c = str[i];
		if (IS_UPPER(c))
			str[i] = ft_tolower(c);
		++i;
	}
}
