#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = ft_strlen(s);
	while (*s)
		s++;
	while (*s != c)
	{
		i--;
		if (i < 0)
			return (NULL);
		s--;
	}
	return ((char *)s);
}
